def suma(num_1, num_2):
    resultado = num_1 + num_2
    return resultado

def resta(num_1, num_2):
    resultado = num_1 - num_2
    return resultado

def multiplicacion(num_1, num_2):
    resultado = num_1 * num_2
    return resultado

def division(num_1, num_2):
    resultado = num_1 / num_2
    return resultado

def potencia(base, exponente):
    resultado = base ** exponente
    return resultado

def redondear(num_1):
    resultado = round(num_1)
    return resultado